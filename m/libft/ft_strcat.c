#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	d;
	int	s;	

	d = 0;
	s = 0;
	while (dest[d] != '\0')
		d++;
	while (src[s] != '\0')
	{
		dest[d] = src[s];
		d++;
		s++;
	}
	dest[d] = '\0';
	return (dest);
}
